package org.telokers.exception;

/**
 * @author trung
 *
 */
public class SecurityException extends Exception {

	public SecurityException() {
		super();
	}

	public SecurityException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SecurityException(String arg0) {
		super(arg0);
	}

	public SecurityException(Throwable arg0) {
		super(arg0);
	}

	/**
	 *
	 */
	private static final long serialVersionUID = 7638304401588823678L;

}
